public class SimplePriority implements BalancePriority,ImportantObject
{
	////////////////////////////////////////////////////////
	/**
	 *
	 */
	////////////////////////////////////////////////////////
	private int miValue = 1;
	private int miSurplus = 0;
	////////////////////////////////////////////////////////
	/**
	 *
	 * @param iValue int
	 */
	////////////////////////////////////////////////////////
	public void setValue(int iValue)
	{
		if(iValue <= 0)
			throw new IllegalArgumentException("Priority value have to be greater than zero");
		miValue = iValue;
	}
	////////////////////////////////////////////////////////
	/**
	 *
	 * @return int
	 */
	////////////////////////////////////////////////////////
	public int getValue()
	{
		return miValue;
	}
	////////////////////////////////////////////////////////
	/**
	 *
	 * @return int
	 */
	////////////////////////////////////////////////////////
	public int getSurplus()
	{
		return miSurplus;
	}
	////////////////////////////////////////////////////////
	/**
	 *
	 * @param iValue int
	 */
	////////////////////////////////////////////////////////
	public void decrease(int iValue)
	{
		miSurplus -= iValue;
	}
	////////////////////////////////////////////////////////
	/**
	 *
	 */
	////////////////////////////////////////////////////////
	public void increase(int iValue)
	{
		miSurplus += iValue;
	}
	////////////////////////////////////////////////////////
	/**
	 *
	 */
	////////////////////////////////////////////////////////
	public void reset()
	{
		miSurplus = 0;
	}
	////////////////////////////////////////////////////////
	/**
	 *
	 * @return Priority
	 */
	////////////////////////////////////////////////////////
	public Priority getPriority()
	{
		return this;
	}
}
